// Ülke ismini Türkçe'ye çevir
export function translateCountry(name: string): string {
  if (!name) return '';
  
  // Önce tam eşleşme ara
  if (countryTranslations[name]) {
    return countryTranslations[name];
  }
  
  // Küçük harfle ara
  const lowerName = name.toLowerCase();
  for (const [key, value] of Object.entries(countryTranslations)) {
    if (key.toLowerCase() === lowerName) {
      return value;
    }
  }
  
  // Bulunamazsa orijinal ismi döndür
  return name;
}

// Birden fazla ülke ismini çevir
export function translateCountries(names: string[]): string[] {
  return names.map(translateCountry);
}
