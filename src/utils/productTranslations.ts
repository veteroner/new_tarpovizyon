/**
 * FAO İngilizce ürün adını Türkçeye çevirir.
 * Birebir eşleşme yoksa orijinal adı döndürür.
 */
export function translateProduct(name: string): string {
  if (!name) return name;
  const exact = faoProductTranslations[name];
  if (exact) return exact;
  // Kısmi eşleşme denemesi (ör. "Wheat, durum" → "Buğday" bulunabilir)
  for (const [en, tr] of Object.entries(faoProductTranslations)) {
    if (en.length > 4 && name.toLowerCase().startsWith(en.toLowerCase())) return tr;
  }
  return name;
}
