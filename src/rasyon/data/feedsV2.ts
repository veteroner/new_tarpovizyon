/**
 * Performance optimization: Map-based feed lookup - O(1) instead of O(n)
 */
export const FEEDS_BY_ID = new Map<string, Feed>(
  feeds.map((feed) => [feed.id, feed])
)

/**
 * Get feed by ID - O(1) lookup using Map
 * @param id Feed identifier
 * @returns Feed object or undefined if not found
 */
export const getFeedById = (id: string): Feed | undefined => {
  return FEEDS_BY_ID.get(id)
}

export const getForageFeeds = (): Feed[] => {
  return feeds.filter((f) => f.category === 'forage')
}

export const getConcentrateFeeds = (): Feed[] => {
  return feeds.filter((f) => f.category === 'concentrate')
}

export const getMineralFeeds = (): Feed[] => {
  return feeds.filter((f) => f.category === 'mineral')
}
